      s"""import { FluencePeer } from '@fluencelabs/fluence';
         |import {
         |    $callParams
         |    callFunction$$,
         |    registerService$$,
         |} from '@fluencelabs/fluence/dist/internal/compilerSupport/v3${if (isJs) ".js"
      else ""}';""".stripMargin
